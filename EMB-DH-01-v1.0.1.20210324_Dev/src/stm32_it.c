void TIM3_IRQHandler(void)
{
	uint8_t i=0,j=0;
	if(TIM_GetITStatus(TIM3, TIM_IT_Update) != RESET)
  {
		//STOP_TIMER;
		TIM_ClearITPendingBit(TIM3, TIM_IT_Update);
		//STOP_TIMER;
		line_switch();            //switch to different lines	
		Delay_time(2);
		for(j = 0;j < TOTAL_GROUP ; j++)          // set two groups to sample 64 channels
		{			
			switch(j)
			{
				case 0:
					FIRST_GROUP;
					break;				
				case 1:
					SECOND_GROUP;
					break;
			}			
			ADC_StartConversion(ADC1);//start adc conversion
      ADC_StartConversion(ADC2);
  		ADC_StartConversion(ADC3);
			ADC_StartConversion(ADC4);          //  wait adc finish all conversion			
		  for(i=0;i<9;i++)          //  get adc data 
			{			
				value_final[sample_time][line_num-1][i+(j<<5)] = *(p1+i);
			}
			for(i=0;i<6;i++)
			{			
				value_final[sample_time][line_num-1][i+9+(j<<5)] = *(p2+i);
			}			
			for(i=0;i<10;i++)	
			{
				value_final[sample_time][line_num-1][i+15+(j<<5)] = *(p3+i);
			}				
			for(i=0;i<7;i++)
			{			
				value_final[sample_time][line_num-1][i+25+(j<<5)] = *(p4+i);
			}				
		}	
		Line_Over_Flag = 1;		    // finish one line
  }
}

